function readMore1() {
    if (document.getElementById("read-more1").className == "read-more-close") {
        document.getElementById("read-more1").className = "read-more-open";
    } else if (document.getElementById("read-more1").className == "read-more-open") {
        document.getElementById("read-more1").className = "read-more-close";
    }
}

